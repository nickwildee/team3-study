//6조각으로 잘라주는 피자를 n명이 먹을때 남기지 않고 모두 같은 조각 수를 먹으려면 몇 판의 피자가 필요한가?

function solution(n) {
  let result = 1; // 결과
  let piece = 6; // 조각
  while (true) {
    if ((result * piece) % n === 0) {
      // 조각 수를 인원으로 나눴을 때 나머지가 0이면 결과
      return result;
    }
    result++; // 아니면 피자 1판 추가
  }
}
