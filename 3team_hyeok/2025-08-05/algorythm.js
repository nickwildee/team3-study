// n명의 사람이 피자 한판(7조각)을 얼마나 먹나?
// n / 7
// ex) 4 / 7 = 0.57...
// 4명의 사람은 7조각의 피자중 57% 가량을 먹는다.
// 피자를 0.57판 시킬 순 없으니 소수를 반올림해서 정수로 만든다.
// Math.ceil(n / 7);
// ex) Math.ceil(0.57...) = 1;
// 1판이면 된다.

// ex) 11 / 7 = 1.57...
// Math.ceil(1.57...) = 2;
// 2판이면 된다.

